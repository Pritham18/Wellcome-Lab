// Helper function to get team members by category
export function getTeamByCategory(category) {
  const members = Object.values(teamMembers).filter(member => member.category === category);
  
  // Custom ordering for leadership section
  if (category === 'leadership') {
    const order = ['chien-fei-chen', 'mingzhou-jin', 'bruce-tonn', 'gary-adamkiewicz', 'kristina-kintziger', 'leon-tolbert'];
    return members.sort((a, b) => {
      const indexA = order.indexOf(a.slug);
      const indexB = order.indexOf(b.slug);
      if (indexA === -1) return 1;
      if (indexB === -1) return -1;
      return indexA - indexB;
    });
  }

  // Custom ordering for research-scientists section
  if (category === 'research-scientists') {
    const order = ['bazgha-zia', 'lyudmyla-tsykalova', 'ziwei-liu', 'shiyuan-fan', 'liao-wang'];
    return members.sort((a, b) => {
      const indexA = order.indexOf(a.slug);
      const indexB = order.indexOf(b.slug);
      if (indexA === -1) return 1;
      if (indexB === -1) return -1;
      return indexA - indexB;
    });
  }
  
  // Custom ordering for research-assistants section
  if (category === 'research-assistants') {
    const order = ['yueyang-feng', 'pritham-mahajan', 'fateh-arina'];
    return members.sort((a, b) => {
      const indexA = order.indexOf(a.slug);
      const indexB = order.indexOf(b.slug);
      // If not in order array, put at end
      if (indexA === -1) return 1;
      if (indexB === -1) return -1;
      return indexA - indexB;
    });
  }
  
  return members;
}

